#include "fillit.h"

char	*ft_erase(int i, char *sol)
{
	int x;

	x = 0;
	while (sol[x] != '\0')
	{
		if (sol[x] == ('A' + i))
			sol[x] = '.';
		x++;
	}
	return (sol);
}

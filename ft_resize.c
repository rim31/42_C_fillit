#include "fillit.h"

int			**ft_resize(int nbr, int **tab)
{
	int i;
	int j;

	j = 0;
	i = 0;
	nbr = nbr - 4;
	while (tab[i][0] != -1)
	{
		j = 0;
		while (j != 3)
		{
			if (tab[i][j] != 1)
			{
				tab[i][j] = tab[i][j] + nbr;
			}
			j++;
		}
		i++;
	}
	return (tab);
}
